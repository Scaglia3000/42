int		ft_sqrt(int nb)
{
	int x;

	x = 0;
	if (nb > 0 && nb < 2147483647)
	{
		while (x < nb)
		{
			if (x * x == nb)
				return (x);
			x++;
		}
	}
	return (0);
}
