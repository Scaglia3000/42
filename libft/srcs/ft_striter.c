#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int len;

	i = 0;
	if (s != NULL && f != NULL)
	{
		len = ft_strlen(s);
		while (i < len)
		{
			f(&s[i]);
			i++;
		}
	}
}
